export default function Product() {
  const {product} = useLoaderData();
  // State for managing image selection and hover effects
  const [selectedImageId, setSelectedImageId] = useState(null);
  const [hoveredImageId, setHoveredImageId] = useState(null);

  const selectedVariant = useOptimisticVariant(
    product.selectedOrFirstAvailableVariant,
    getAdjacentAndFirstAvailableVariants(product),
  );

  // Group product images by color based on filename pattern
  // This creates an object where keys are colors and values are arrays of images
  const imagesByColor = product.images.nodes.reduce((acc, image) => {
    const colorMatch = image.url.toLowerCase().match(/\/(yellow|blue|green|navy|orange|pink)[-_]/);
    if (colorMatch) {
      const color = colorMatch[1];
      if (!acc[color]) acc[color] = [];
      acc[color].push(image);
    }
    return acc;
  }, {});

  // Extract the current color from the selected variant's options
  const currentColor = selectedVariant?.selectedOptions.find(
    (option) => option.name === 'Color'
  )?.value.toLowerCase();

  // Get images for current color or fallback to all product images
  const currentColorImages = currentColor && imagesByColor[currentColor] 
    ? imagesByColor[currentColor] 
    : product.images.nodes;

  // Reset selected image when color variant changes
  useEffect(() => {
    setSelectedImageId(null);
  }, [currentColor]);

  // Determine the main image to display based on selection or default
  const mainImage = selectedImageId
    ? product.images.nodes.find(img => img.id === selectedImageId)
    : selectedVariant?.image || product.images.nodes[0];

  // Sets the search param to the selected variant without navigation
  // only when no search params are set in the url
  useSelectedOptionInUrlParam(selectedVariant.selectedOptions);

  // Get the product options array
  const productOptions = getProductOptions({
    ...product,
    selectedOrFirstAvailableVariant: selectedVariant,
  });

  const {title, descriptionHtml} = product;

  return (
    <div className="product">
      <div className="product-images">
        <div 
          onMouseEnter={() => {
            // Show next image in sequence on hover
            const currentIndex = currentColorImages.findIndex(img => img.id === (selectedImageId || mainImage.id));
            const nextImage = currentColorImages[(currentIndex + 1) % currentColorImages.length];
            setHoveredImageId(nextImage.id);
          }}
          onMouseLeave={() => setHoveredImageId(null)}
        >
          <ProductImage 
            image={hoveredImageId 
              ? currentColorImages.find(img => img.id === hoveredImageId)
              : mainImage
            } 
          />
        </div>
        {/* Thumbnail grid for image selection */}
        <div className="product-thumbnail-grid">
          {currentColorImages.map((image) => (
            <div 
              key={image.id} 
              className={`thumbnail ${selectedImageId === image.id ? 'selected' : ''}`}
              onClick={() => setSelectedImageId(image.id)}
            >
              <Image
                data={image}
                sizes="120px"
                className="thumbnail-image"
              />
            </div>
          ))}
        </div>
      </div>
      <div className="product-main">
        <h1>{title}</h1>
        <ProductPrice
          price={selectedVariant?.price}
          compareAtPrice={selectedVariant?.compareAtPrice}
        />
        <br />
        <ProductForm
          productOptions={productOptions}
          selectedVariant={selectedVariant}
        />
        <br />
        <br />
        <p>
          <strong>Description</strong>
        </p>
        <br />
        <div dangerouslySetInnerHTML={{__html: descriptionHtml}} />
        <br />
      </div>
      <Analytics.ProductView
        data={{
          products: [
            {
              id: product.id,
              title: product.title,
              price: selectedVariant?.price.amount || '0',
              vendor: product.vendor,
              variantId: selectedVariant?.id || '',
              variantTitle: selectedVariant?.title || '',
              quantity: 1,
            },
          ],
        }}
      />
    </div>
  );
}

